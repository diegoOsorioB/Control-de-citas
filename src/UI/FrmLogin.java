    private void login() {
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText().trim();

        if (username.isEmpty()) {
            Utils.showWarning("El campo username es requerido");
            return;
        }

        if (password.isEmpty()) {
            Utils.showWarning("El campo password es requerido");
            return;
        }

        Usuario usuarioLogged = UsuarioBLL.login(username, password);
        if (usuarioLogged != null) {
            if (usuarioLogged.getRol().getEstatus().equals("Vigente")) {
                this.dispose();
                frmPrincipal = new FrmPrincipal();
                FrmPrincipal.usuarioLogged = usuarioLogged;
                frmPrincipal.inicializar();
                frmPrincipal.setVisible(true);
            } else {
                Utils.showError("El ROL asignado a tu usuario no est√° vigente");
            }
        } else {
            if (UsuarioBLL.list("").isEmpty()) {
                Usuario _default = UsuarioBLL.createDefault();
                if (_default != null) {
                    Utils.showInformation("Se ha creado el administrador por defecto"
                            + "\nusername: " + _default.getUsername()
                            + "\nPassword: " + _default.getPassword());
                }

            } else {
                Utils.showError("Credenciales incorrectas");
            }
        }

    }

